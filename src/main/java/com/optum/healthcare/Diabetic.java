package com.optum.healthcare;

public class Diabetic {
	
	int noOfPregencies;
	int glucose;
	int bp;
	int thickness;
	int insulin;
	double bmi;
	double pedigree;
	int age;
	public int getNoOfPregencies() {
		return noOfPregencies;
	}
	public void setNoOfPregencies(int noOfPregencies) {
		this.noOfPregencies = noOfPregencies;
	}
	public int getGlucose() {
		return glucose;
	}
	public void setGlucose(int glucose) {
		this.glucose = glucose;
	}
	public int getBp() {
		return bp;
	}
	public void setBp(int bp) {
		this.bp = bp;
	}
	public int getThickness() {
		return thickness;
	}
	public void setThickness(int thickness) {
		this.thickness = thickness;
	}
	public int getInsulin() {
		return insulin;
	}
	public void setInsulin(int insulin) {
		this.insulin = insulin;
	}
	public double getBmi() {
		return bmi;
	}
	public void setBmi(double bmi) {
		this.bmi = bmi;
	}
	public double getPedigree() {
		return pedigree;
	}
	public void setPedigree(double pedigree) {
		this.pedigree = pedigree;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
